int	ft_sqrt(int nb)
{
	long long	n;
	long long	ans;

	n = 0;
	ans = 0;
	while (n * n <= nb)
	{
		if (n * n == nb)
				ans = n;
		else
			ans = 0;
		n++;
	}
	return (ans);
}
